async function loadDatabaseQuestions() {
  setStatus(text.loading);
  renderLoadingState();
  try {
    const response = await fetch("/api/questions");
    const data = await response.json();
    if (!response.ok) {
      throw new Error(data.detail || text.failed_load);
    }
    databaseQuestions = data.questions || [];
    updateStats(databaseQuestions);
    updateFilters(databaseQuestions);
    if (pendingPaperFilter) {
      applyPendingPaperFilter();
    } else if (pendingTypeFilter) {
      applyPendingTypeFilter();
    } else {
      renderDatabaseQuestions();
    }
    setStatus(text.loaded);
  } catch (error) {
    databaseQuestions = [];
    updateStats(databaseQuestions);
    renderDatabaseQuestions();
    setStatus(error.message);
  }
}

