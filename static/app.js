function applyMistakeImageDeskew(container) {
  container.querySelectorAll("img[data-deskew='true']").forEach((image) => {
    if (image.dataset.deskewBound === "true") {
      return;
    }
    image.dataset.deskewBound = "true";

    const run = () => deskewMistakeImage(image);
    if (image.complete && image.naturalWidth) {
      run();
    } else {
      image.addEventListener("load", run, { once: true });
    }
  });
}

function deskewMistakeImage(image) {
  if (image.dataset.deskewDone === "true" || image.naturalWidth < 40 || image.naturalHeight < 40) {
    return;
  }

  image.classList.add("deskew-pending");
  try {
    const angle = estimateImageSkewAngle(image);
    image.dataset.deskewDone = "true";
    if (Math.abs(angle) < 0.35) {
      return;
    }

    image.src = rotateImageToDataUrl(image, angle);
    image.classList.add("deskewed");
  } catch (_error) {
    image.dataset.deskewDone = "true";
  } finally {
    image.classList.remove("deskew-pending");
  }
}

function estimateImageSkewAngle(image) {
  const maxSide = 760;
  const scale = Math.min(1, maxSide / Math.max(image.naturalWidth, image.naturalHeight));
  const width = Math.max(1, Math.round(image.naturalWidth * scale));
  const height = Math.max(1, Math.round(image.naturalHeight * scale));
  const canvas = document.createElement("canvas");
  canvas.width = width;
  canvas.height = height;
  const context = canvas.getContext("2d", { willReadFrequently: true });
  context.drawImage(image, 0, 0, width, height);

  const pixels = context.getImageData(0, 0, width, height).data;
  const grayValues = [];
  let graySum = 0;
  const stride = 2;

  for (let y = 0; y < height; y += stride) {
    for (let x = 0; x < width; x += stride) {
      const index = (y * width + x) * 4;
      const gray = pixels[index] * 0.299 + pixels[index + 1] * 0.587 + pixels[index + 2] * 0.114;
      grayValues.push(gray);
      graySum += gray;
    }
  }

  if (!grayValues.length) {
    return 0;
  }

  const averageGray = graySum / grayValues.length;
  const threshold = Math.max(55, Math.min(185, averageGray - 34));
  const points = [];
  for (let y = 0; y < height; y += stride) {
    for (let x = 0; x < width; x += stride) {
      const index = (y * width + x) * 4;
      const gray = pixels[index] * 0.299 + pixels[index + 1] * 0.587 + pixels[index + 2] * 0.114;
      if (gray < threshold) {
        points.push([x - width / 2, y - height / 2]);
      }
    }
  }

  if (points.length < 120) {
    return 0;
  }

  const sampledPoints = points.length > 9000 ? points.filter((_, index) => index % Math.ceil(points.length / 9000) === 0) : points;
  const coarse = findBestSkewAngle(sampledPoints, -12, 12, 0.5);
  const refined = findBestSkewAngle(sampledPoints, coarse.angle - 0.7, coarse.angle + 0.7, 0.1);
  return refined.angle;
}

function findBestSkewAngle(points, minAngle, maxAngle, step) {
  let bestAngle = 0;
  let bestScore = Number.NEGATIVE_INFINITY;

  for (let angle = minAngle; angle <= maxAngle + 0.0001; angle += step) {
    const radians = (angle * Math.PI) / 180;
    const sin = Math.sin(radians);
    const cos = Math.cos(radians);
    const rows = new Map();

    points.forEach(([x, y]) => {
      const rotatedY = Math.round(x * sin + y * cos);
      rows.set(rotatedY, (rows.get(rotatedY) || 0) + 1);
    });

    let score = 0;
    rows.forEach((count) => {
      score += count * count;
    });

    if (score > bestScore) {
      bestScore = score;
      bestAngle = angle;
    }
  }

  return { angle: bestAngle, score: bestScore };
}

function rotateImageToDataUrl(image, angle) {
  const radians = (angle * Math.PI) / 180;
  const sin = Math.abs(Math.sin(radians));
  const cos = Math.abs(Math.cos(radians));
  const width = image.naturalWidth;
  const height = image.naturalHeight;
  const rotatedWidth = Math.ceil(width * cos + height * sin);
  const rotatedHeight = Math.ceil(width * sin + height * cos);
  const canvas = document.createElement("canvas");
  canvas.width = rotatedWidth;
  canvas.height = rotatedHeight;
  const context = canvas.getContext("2d");
  context.fillStyle = "#ffffff";
  context.fillRect(0, 0, rotatedWidth, rotatedHeight);
  context.translate(rotatedWidth / 2, rotatedHeight / 2);
  context.rotate(radians);
  context.drawImage(image, -width / 2, -height / 2);
  return canvas.toDataURL("image/png");
}

