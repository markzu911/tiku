function renderMathText(value) {
  return escapeHtml(value).replace(
    /(^|[^\w.])(\d{1,3})\s*\/\s*(\d{1,3})(?![\w.])/g,
    '$1<span class="math-fraction"><span>$2</span><span>$3</span></span>',
  );
}

