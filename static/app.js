function applyPendingTypeFilter() {
  if (!pendingTypeFilter) {
    return;
  }

  typeFilter.value = pendingTypeFilter;
  if (typeFilter.value !== pendingTypeFilter) {
    pendingTypeFilter = "";
    return;
  }

  searchInput.value = "";
  gradeFilter.value = "";
  categoryFilter.value = "";
  activePaperFilter = null;
  pendingTypeFilter = "";
  renderDatabaseQuestions();
}

function applyPendingPaperFilter() {
  if (!pendingPaperFilter) {
    return;
  }

  activePaperFilter = pendingPaperFilter;
  pendingPaperFilter = null;
  searchInput.value = "";
  gradeFilter.value = "";
  categoryFilter.value = "";
  typeFilter.value = "";
  renderDatabaseQuestions();
}

async function loadDatabaseQuestions() {
  setStatus(text.loading);
  renderLoadingState();
  try {
    const response = await fetch("/api/questions");
    const data = await response.json();
    if (!response.ok) {
      throw new Error(data.detail || text.failed_load);
    }
    databaseQuestions = data.questions || [];
    updateStats(databaseQuestions);
    updateFilters(databaseQuestions);
    if (pendingPaperFilter) {
      applyPendingPaperFilter();
    } else if (pendingTypeFilter) {
      applyPendingTypeFilter();
    } else {
      renderDatabaseQuestions();
    }
    setStatus(text.loaded);
  } catch (error) {
    databaseQuestions = [];
    updateStats(databaseQuestions);
    renderDatabaseQuestions();
    setStatus(error.message);
  }
}

