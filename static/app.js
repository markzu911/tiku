paperList.addEventListener("click", (event) => {
  const carouselButton = event.target.closest(".paper-carousel-button");
  if (carouselButton) {
    event.stopPropagation();
    rotatePaperSlide(carouselButton.closest(".paper-card"), carouselButton.classList.contains("next") ? 1 : -1);
    return;
  }

  const card = event.target.closest(".paper-card[data-paper-ids]");
  if (!card) return;
  openQuestionsByPaper(card.dataset.paperIds.split(","), card.dataset.paperName);
});

paperList.addEventListener("keydown", (event) => {
  const card = event.target.closest(".paper-card[data-paper-ids]");
  if (!card) return;

  if (event.key === "ArrowLeft" || event.key === "ArrowRight") {
    event.preventDefault();
    rotatePaperSlide(card, event.key === "ArrowRight" ? 1 : -1);
    return;
  }

  if (event.key !== "Enter" && event.key !== " ") return;
  event.preventDefault();
  openQuestionsByPaper(card.dataset.paperIds.split(","), card.dataset.paperName);
});

typeList.addEventListener("click", (event) => {
  const card = event.target.closest(".type-card[data-question-type]");
  if (!card) return;
  openQuestionsByType(card.dataset.questionType);
});

typeList.addEventListener("keydown", (event) => {
  if (event.key !== "Enter" && event.key !== " ") return;
  const card = event.target.closest(".type-card[data-question-type]");
  if (!card) return;
  event.preventDefault();
  openQuestionsByType(card.dataset.questionType);
});

