function clearFilters() {
  searchInput.value = "";
  gradeFilter.value = "";
  categoryFilter.value = "";
  typeFilter.value = "";
  activePaperFilter = null;
  pendingPaperFilter = null;
  pendingTypeFilter = "";
  renderDatabaseQuestions();
}

function switchHeader(viewName) {
  const config = viewConfig[viewName] || viewConfig.upload;
  pageTitle.textContent = config.title;
  pageSubtitle.textContent = config.subtitle;
}

function switchView(viewName) {
  const config = viewConfig[viewName] || viewConfig.upload;
  Object.entries(viewConfig).forEach(([name, item]) => {
    item.node.classList.toggle("active", name === viewName);
  });
  navItems.forEach((item) => item.classList.toggle("active", item.dataset.view === viewName));
  switchHeader(viewName);
  setStatus(config.status);

  if (typeof config.onEnter === "function") {
    config.onEnter();
  }
}

function setPreview(files) {
  const fileList = Array.from(files || []);
  if (fileList.length === 0) {
    previewWrap.hidden = true;
    previewImage.removeAttribute("src");
    previewCaption.textContent = "";
    return;
  }
  const file = fileList[0];
  previewImage.src = URL.createObjectURL(file);
  previewCaption.textContent =
    fileList.length === 1
      ? file.name
      : `\u5df2\u9009\u62e9 ${fileList.length} \u5f20\uff0c\u9884\u89c8\uff1a${file.name}`;
  previewWrap.hidden = false;
  setStatus(fileList.length === 1 ? file.name : `\u5df2\u9009\u62e9 ${fileList.length} \u5f20\u8bd5\u5377`);
}

navItems.forEach((item) => {
  item.addEventListener("click", () => {
    if (item.dataset.view === "questions") {
      activePaperFilter = null;
      pendingPaperFilter = null;
      pendingTypeFilter = "";
    }
    switchView(item.dataset.view);
  });
});

