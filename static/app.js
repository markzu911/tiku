function rotatePaperSlide(card, direction) {
  if (!card) return;
  const preview = card.querySelector(".paper-preview");
  const slides = [...card.querySelectorAll(".paper-slide")];
  const indicator = card.querySelector(".paper-page-indicator");
  if (!preview || slides.length <= 1) return;

  const current = Number(preview.dataset.currentSlide || 0);
  const next = (current + direction + slides.length) % slides.length;
  slides.forEach((slide, index) => slide.classList.toggle("active", index === next));
  preview.dataset.currentSlide = String(next);
  if (indicator) {
    indicator.textContent = `${next + 1} / ${slides.length}`;
  }
}

function formatPercent(value) {
  const number = Number(value || 0);
  return `${number.toFixed(2)}%`;
}

function getMasteryLabel(accuracy) {
  const value = Number(accuracy || 0);
  if (value >= 100) {
    return { text: text.mastery_perfect, level: "perfect" };
  }
  if (value > 50) {
    return { text: text.mastery_occasional, level: "occasional" };
  }
  return { text: text.mastery_practice, level: "practice" };
}

