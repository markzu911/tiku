async function loadPapers() {
  setStatus(text.loading_papers);
  renderPaperLoadingState();
  try {
    const response = await fetch("/api/papers");
    const data = await response.json();
    if (!response.ok) {
      throw new Error(data.detail || text.failed_load_papers);
    }
    papers = data.papers || [];
    renderPapers();
    setStatus(text.loaded_papers);
  } catch (error) {
    papers = [];
    renderPapers();
    setStatus(error.message);
  }
}

function openQuestionsByPaper(paperIds, paperName) {
  if (!paperIds.length) {
    return;
  }

  pendingTypeFilter = "";
  pendingPaperFilter = {
    paperIds: paperIds.map((id) => Number(id)).filter(Boolean),
    name: paperName || text.unknown,
  };
  switchView("questions");
}

async function loadQuestionTypes() {
  setStatus(text.loading_types);
  renderTypeLoadingState();
  try {
    const response = await fetch("/api/question-types");
    const data = await response.json();
    if (!response.ok) {
      throw new Error(data.detail || text.failed_load_types);
    }
    questionTypes = data.question_types || [];
    updateTypeStats(questionTypes);
    renderQuestionTypes();
    setStatus(text.loaded_types);
  } catch (error) {
    questionTypes = [];
    updateTypeStats(questionTypes);
    renderQuestionTypes();
    setStatus(error.message);
  }
}

